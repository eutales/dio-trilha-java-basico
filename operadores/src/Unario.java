public class Unario {
    //Operador negação !
    //Operador unário +, -, ++, --
    public static void main(String[] args) {
        //Operador unário, fazendo com que o número se torne negativo.  
        int numero = 5;
         numero=-numero;
         System.out.println(numero);

//Decremento do valor total;
         numero = 5;
         numero -= numero;
         System.out.println(numero);
//Incremento unitátio
         numero = 5;
         numero++; //Resgata o valor e incrementa.
         System.out.println(numero);
    }

   

}
